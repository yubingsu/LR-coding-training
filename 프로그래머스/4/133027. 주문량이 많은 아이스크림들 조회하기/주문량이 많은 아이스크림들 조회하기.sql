SELECT H.FLAVOR
FROM FIRST_HALF H -- 1집합
JOIN JULY J -- M집합
ON H.FLAVOR = J.FLAVOR -- M집합 ( j)  
GROUP BY FLAVOR
ORDER BY SUM(H.TOTAL_ORDER) + SUM(J.TOTAL_ORDER) DESC
LIMIT 3




